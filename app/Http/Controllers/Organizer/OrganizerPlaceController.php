    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Place  $place
     * @return \Illuminate\Http\Response
     */
    public function destroy(Place $place, $id)
    {
        $place = Place::find($id);
        $place->delete();

        return redirect()->route('organizer.places.index');
    }
